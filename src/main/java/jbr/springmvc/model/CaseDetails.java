package jbr.springmvc.model;

public class CaseDetails {
	
	private String case_id;
	private String category;
	private String name;
	private String channel;
	private String submitter;
	private String status;
	private String r_date;
	private String a_date;
	private String drug_name;
	private String drug_desc;
	private String rev_comments;
	private String case_docs;
	private String ap_date;
	private String detailed_desc;
	
	public String getCase_id() {
		return case_id;
	}
	public void setCase_id(String case_id) {
		this.case_id = case_id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public String getSubmitter() {
		return submitter;
	}
	public void setSubmitter(String submitter) {
		this.submitter = submitter;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getR_date() {
		return r_date;
	}
	public void setR_date(String r_date) {
		this.r_date = r_date;
	}
	public String getA_date() {
		return a_date;
	}
	public void setA_date(String a_date) {
		this.a_date = a_date;
	}
	public String getDrug_name() {
		return drug_name;
	}
	public void setDrug_name(String drug_name) {
		this.drug_name = drug_name;
	}
	public String getDrug_desc() {
		return drug_desc;
	}
	public void setDrug_desc(String drug_desc) {
		this.drug_desc = drug_desc;
	}
	public String getRev_comments() {
		return rev_comments;
	}
	public void setRev_comments(String rev_comments) {
		this.rev_comments = rev_comments;
	}
	public String getCase_docs() {
		return case_docs;
	}
	public void setCase_docs(String case_docs) {
		this.case_docs = case_docs;
	}
	public String getAp_date() {
		return ap_date;
	}
	public void setAp_date(String ap_date) {
		this.ap_date = ap_date;
	}
	public String getDetailed_desc() {
		return detailed_desc;
	}
	public void setDetailed_desc(String detailed_desc) {
		this.detailed_desc = detailed_desc;
	}
	

}